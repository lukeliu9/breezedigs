$(function() {
			$("li#cons").click(function(e) {
			  e.preventDefault();
			  $("li.review-btn-selected").removeClass("review-btn-selected");
			  $(this).addClass("review-btn-selected");
			  $("div.unhidden").removeClass("unhidden")
			  $("div#cons").addClass("unhidden")
			  $("div.review:empty").addClass("hidden")
			});
		});

